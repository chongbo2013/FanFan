public class Params {

    private String sub;
    private boolean lrst;
    private int rstid;

    public String getSub() {
        return sub;
    }

    public void setSub(String sub) {
        this.sub = sub;
    }

    public boolean isLrst() {
        return lrst;
    }

    public void setLrst(boolean lrst) {
        this.lrst = lrst;
    }

    public int getRstid() {
        return rstid;
    }

    public void setRstid(int rstid) {
        this.rstid = rstid;
    }
}
